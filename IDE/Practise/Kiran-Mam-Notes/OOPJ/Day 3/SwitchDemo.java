class SwitchDemo {
    public static void main(String[] args) {
		
		long ch = 1;//
		switch(ch){
			case 55:
				System.out.println("Saturday");break;
			case 1:
				System.out.println("Monday");break; 
			case 5:
				System.out.println("Saturday");break; 
			default:
				System.out.println("Sunday");break;
		}
		
		
		
		
       
    }
}