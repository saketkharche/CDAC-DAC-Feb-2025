class SwitchDemo1 {
    public static void main(String[] args) {
		
		String ch = "a";
		
		switch(ch){
			case "a":
				System.out.println("Saturday");break;
			case "b":
				System.out.println("Monday");break; 
			case "5":
				System.out.println("Saturday");break; 
			default:
				System.out.println("Sunday");break;
		}
		
		
		
		
       
    }
}