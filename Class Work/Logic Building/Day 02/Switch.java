class Switch{
  public static void main(String[] args) {
    int a = 10;
    switch (a) {
      case 10:
        System.out.println("10");
        break;

      case 20:
        System.out.println("10");
        break;

      case 30:
        System.out.println("10");
        break;

      default:
        System.out.println("default case");
        break;


    }
    System.out.println("outside switch");
  }
}
