class Q6Basic_Arithmetic_Operations {
  public static void main(String[] args) {
    int a = 125;
    int b = 24;

    int c = 125 + 24;
    int d = 125 - 24;
    int e = 125 * 24;
    int f = 125 / 24;
    int g = 125 % 24;

    System.out.println(a + " + " + b + " = " + c);
    System.out.println(a + " - " + b + " = " + d);
    System.out.println(a + " x " + b + " = " + e);
    System.out.println(a + " / " + b + " = " + f);
    System.out.println(a + " % " + b + " = " + g);
  }
}
