class Q1SumOfNaturalNumbers {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 50; i++) {
            sum += i;
        }
        System.out.println("Sum of first 50 natural numbers: " + sum);
    }
}
// Sample Output: Sum of first 50 natural numbers: 1275
